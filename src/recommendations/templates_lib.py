def get_template(name: str) -> InstructionTemplate:
    if name not in TEMPLATES:
        return TEMPLATES["manual_review"]
    return TEMPLATES[name]


def template_vars_merge(*parts: dict[str, Any]) -> dict[str, Any]:
    out: dict[str, Any] = {}
    for part in parts:
        if part:
            out.update(part)
    return out
